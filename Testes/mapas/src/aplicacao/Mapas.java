public class Mapas {

    int tamanho_x;
    int tamanho_y;
    int posicao_x;
    int posicao_y;
    String terreno;
    String nome_posicao;
    String Nome_mapa;

    public int getTamanho_x() {
        return tamanho_x;
    }

    public void setTamanho_x(int tamanho_x) {
        this.tamanho_x = tamanho_x;
    }

    public int getTamanho_y() {
        return tamanho_y;
    }

    public void setTamanho_y(int tamanho_y) {
        this.tamanho_y = tamanho_y;
    }

    public int getPosicao_x() {
        return posicao_x;
    }

    public void setPosicao_x(int posicao_x) {
        this.posicao_x = posicao_x;
    }

    public int getPosicao_y() {
        return posicao_y;
    }

    public void setPosicao_y(int posicao_y) {
        this.posicao_y = posicao_y;
    }

    public String getTerreno() {
        return terreno;
    }

    public void setTerreno(String terreno) {
        this.terreno = terreno;
    }

    public String getNome_posicao() {
        return nome_posicao;
    }

    public void setNome_posicao(String nome_posicao) {
        this.nome_posicao = nome_posicao;
    }

    public String getNome_mapa() {
        return Nome_mapa;
    }

    public void setNome_mapa(String Nome_mapa) {
        this.Nome_mapa = Nome_mapa;
    }

}
